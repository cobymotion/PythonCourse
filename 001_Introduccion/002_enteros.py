x = 52 
print(type(x)) # Da como resultado int 

x = 0o52
print (x) #considera su entrada como octal y lo muestra en decimal
print(type(x)) # Da como resultado int 

x=0xA0F
print (x) #considera su entrada como hexadecimal y lo muestra en decimal
print(type(x)) # Da como resultado int 

x= 2.1 + 7.8j
print(type(x)) # Da como resultado complex 

x= -045.3
print(type(x)) # Da como resultado float
